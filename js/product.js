// RGB → HSV
function rgbToHsv(r, g, b) {
  r /= 255; g /= 255; b /= 255;
  const max = Math.max(r,g,b), min = Math.min(r,g,b);
  const d = max - min;
  let h = 0;

  if (d !== 0) {
    if (max === r) h = ((g - b) / d) % 6;
    else if (max === g) h = (b - r) / d + 2;
    else h = (r - g) / d + 4;
    h *= 60;
    if (h < 0) h += 360;
  }

  const s = max === 0 ? 0 : d / max;
  const v = max;

  return { h, s, v };
}

// DETECT COLOR FAMILY
function getColorName(r, g, b) {

  const { h, s, v } = rgbToHsv(r, g, b);

  if (v < 0.2) return "black";
  if (v > 0.9 && s < 0.25) return "white";

  if (s < 0.25) {
    if (v < 0.6) return "brown";
    return "white";
  }

  if (h >= 35 && h <= 65) return "yellow";
  if (h > 65 && h <= 160) return "green";
  if (h > 160 && h <= 260) return "blue";
  if (h > 260 && h <= 320) return "purple";
  if (h < 20 || h > 340) return "red";

  return "brown";
}

captureBtn.onclick = () => {

  if (!video.videoWidth) return;

  canvas.width = video.videoWidth;
  canvas.height = video.videoHeight;

  ctx.drawImage(video, 0, 0);

  const data = ctx.getImageData(
    canvas.width * 0.3,
    canvas.height * 0.3,
    canvas.width * 0.4,
    canvas.height * 0.4
  ).data;

  let r = 0, g = 0, b = 0, count = 0;

  for (let i = 0; i < data.length; i += 4) {
    r += data[i];
    g += data[i+1];
    b += data[i+2];
    count++;
  }

  r = Math.round(r / count);
  g = Math.round(g / count);
  b = Math.round(b / count);

  const detected = getColorName(r, g, b);

  console.log("Detected:", detected);

  filterColorsByFamily(detected);

  // Close camera
  cameraModal.style.display = "none";
  if (stream) stream.getTracks().forEach(t => t.stop());
};